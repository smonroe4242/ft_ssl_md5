#include "libft.h"

void	ft_strrev(char *s)
{
	int	l;
	int	i;

	l = ft_strlen(s);
	i = -1;
	while (++i < l / 2)
	{
		s[l - i - 1] ^= s[i];
		s[i] ^= s[l - i - 1];
		s[l - i - 1] ^= s[i];
	}
}
